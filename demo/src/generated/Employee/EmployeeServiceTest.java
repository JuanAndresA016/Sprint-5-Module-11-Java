package com.ai.crud.service;

import com.ai.crud.entity.Employee;
import com.ai.crud.repository.EmployeeRepository;
import jakarta.transaction.Transactional;
import org.junit.jupiter.api.*;
import org.mockito.InjectMocks;
import org.mockito.Mock;
import org.mockito.MockitoAnnotations;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

@Service
public class EmployeeServiceTest {

    @Mock
    private EmployeeRepository employeeRepository;

    @InjectMocks
    private EmployeeService employeeService;

    @Autowired
    private EmployeeRepository mockEmployeeRepository;

    @BeforeEach
    public void setUp() {
        MockitoAnnotations.openMocks(this);
    }

    // Happy Path Tests

    @Test
    public void givenValidId_whenFindById_thenReturnEntity() {
        Long id = 1L;
        Employee employee = new Employee();
        when(mockEmployeeRepository.findById(id)).thenReturn(employee);

        Employee result = employeeService.findById(id);

        assertEquals(employee, result);
    }

    @Test
    public void givenValidEmployeeData_whenSaveEmployee_thenReturnSavedEmployee() {
        Employee employee = new Employee();
        employee.setId(1L); // Assuming id is auto-generated by the database

        when(mockEmployeeRepository.save(any(Employee.class))).thenReturn(employee);

        Employee savedEmployee = employeeService.saveEmployee(employee);

        assertEquals(employee, savedEmployee);
    }

    @Test
    public void givenValidId_whenDeleteById_thenNoException() {
        Long id = 1L;
        doNothing().when(mockEmployeeRepository).deleteById(id);

        employeeService.deleteEmployeeById(id);

        verify(mockEmployeeRepository, times(1)).deleteById(id);
    }

    // Edge Case Tests

    @Test
    public void givenNullId_whenDeleteById_thenThrowException() {
        assertThrows(IllegalArgumentException.class, () -> employeeService.deleteEmployeeById(null));
    }

    @Test
    public void givenInvalidId_whenFindById_thenReturnNull() {
        Long id = 1L;
        when(mockEmployeeRepository.findById(id)).thenReturn(null);

        Employee result = employeeService.findById(id);

        assertNull(result);
    }

    // Exception Scenarios

    @Test
    public void givenValidEmployeeData_whenSaveEmployee_thenNoException() {
        Employee employee = new Employee();
        employee.setId(1L); // Assuming id is auto-generated by the database

        doNothing().when(mockEmployeeRepository).save(any(Employee.class));

        employeeService.saveEmployee(employee);
    }

    @Test
    public void givenNullEmployeeData_whenSaveEmployee_thenThrowException() {
        assertThrows(IllegalArgumentException.class, () -> employeeService.saveEmployee(null));
    }
}
